# Definition for a binary tree node.
# class TreeNode:
#     def __init__(self, val=0, left=None, right=None):
#         self.val = val
#         self.left = left
#         self.right = right
class Solution:
    def zigzagLevelOrder(self, root: Optional[TreeNode]) -> List[List[int]]:
        if not root:
            return []
        stack = [root]
        res = []
        while stack:
            n = len(stack)
            tmp = []
            for _ in range(n):
                last = stack.pop(0)
                if last.left:
                    stack.append(last.left)
                if last.right:
                    stack.append(last.right)
                tmp.append(last.val)
            if len(res) % 2 == 0:
                res.append(tmp)
            else:
                res.append(tmp[::-1])
        return res
